package gr.cite.scm.plugin.oidc;

import org.apache.shiro.SecurityUtils;
import sonia.scm.user.User;

import javax.ws.rs.GET;
import javax.ws.rs.Path;
import javax.ws.rs.Produces;
import javax.ws.rs.core.MediaType;

@Path("oidc/profile")
public class OidcProfileResource {

    private User user;

    public OidcProfileResource() {
        this.user = SecurityUtils.getSubject().getPrincipals().oneByType(User.class);
    }

    @GET()
    @Path("info")
    @Produces({MediaType.APPLICATION_JSON})
    public ProfileInfoResponse getInfo() {
        return new ProfileInfoResponse(user.getName(), user.getDisplayName(), user.getMail());
    }

    private static class ProfileInfoResponse {

        private String username, displayName, email;

        private ProfileInfoResponse(String username, String displayName, String email) {
            this.username = username;
            this.displayName = displayName;
            this.email = email;
        }

        public String getUsername() {
            return username;
        }

        public void setUsername(String username) {
            this.username = username;
        }

        public String getDisplayName() {
            return displayName;
        }

        public void setDisplayName(String displayName) {
            this.displayName = displayName;
        }

        public String getEmail() {
            return email;
        }

        public void setEmail(String email) {
            this.email = email;
        }
    }

}
